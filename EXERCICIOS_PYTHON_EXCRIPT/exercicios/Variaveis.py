a = 10 # declarando a variável 'a' e atribuindo-lhe o valor 10
b = 25 # declarando a variável 'b' e atribuindo-lhe o valor 25
c = a + b # declarando a variável 'c' e atribuindo-lhe o valor de 'a' + 'b'
texto = "Isto é um texto - String"

"""
    Imprimindo os valores armazenados nas variáveis declaradas
"""
print(a)
print(b)
print(c)
print(a + b + c)
print(texto)

"""
    Descobrindo os tipos das variáveis declaradas
"""
print(type(a))
print(type(b))
print(type(c))
print(type(texto))