"""
    Dicionários são tipos de dados compostos, onde seus elementos são
    formados por pares de chave e valor
"""

# Uma maneira de criar um dicionário é começando com um dicionário vazio e ir adicionando valores
ing_port = {}
ing_port['one'] = 'um'
ing_port['two'] = 'dois'
ing_port['three'] = 'três'
ing_port['four'] = 'quatro'

# Outra maneira de criar um dicionário é fornecendo uma lista de pares chave e valor
ing_port = {'one':'um', 'two':'dois', 'three':'três', 'four':'quatro'}

print(ing_port)
print(ing_port['two'])
